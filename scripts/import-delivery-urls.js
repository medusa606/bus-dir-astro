#!/usr/bin/env node
//
// Generates scripts/upload-delivery-urls.sql for running in the Supabase SQL editor.
// This sidesteps the curation_audit trigger (which blocks API-level updates)
// by disabling it, bulk-updating all 300+ rows in one statement, then re-enabling.
//
// Usage: node scripts/import-delivery-urls.js
// Then:  paste the generated SQL into the Supabase SQL editor and run it.

import fs from 'fs';
import { parse } from 'csv-parse/sync';

function escape(val) {
  // Escape single quotes for SQL string literals
  return val.replace(/'/g, "''");
}

function nullIfEmpty(val) {
  return val && val.trim() !== '' ? val.trim() : null;
}

console.log('📖 Reading CSV...');
const content = fs.readFileSync('./db_backup./listings_rows-08.csv', 'utf-8');
const records = parse(content, { columns: true, skip_empty_lines: true });

console.log(`✅ Loaded ${records.length} total records from CSV`);

const deliveryRows = records
  .filter(r => nullIfEmpty(r.deliveroo_url) || nullIfEmpty(r.uber_eats_url))
  .map(r => ({
    id: r.id,
    delivery_deliveroo: nullIfEmpty(r.deliveroo_url),
    delivery_ubereats: nullIfEmpty(r.uber_eats_url),
  }));

console.log(`🚚 Found ${deliveryRows.length} rows with delivery URLs\n`);

if (deliveryRows.length === 0) {
  console.error('⚠️  No delivery URLs found — check CSV column names (expected: deliveroo_url, uber_eats_url)');
  process.exit(1);
}

// Build a VALUES list: (id, deliveroo_url, ubereats_url)
const valuesList = deliveryRows.map(r => {
  const deliveroo = r.delivery_deliveroo ? `'${escape(r.delivery_deliveroo)}'` : 'NULL';
  const ubereats  = r.delivery_ubereats  ? `'${escape(r.delivery_ubereats)}'`  : 'NULL';
  return `  ('${r.id}'::uuid, ${deliveroo}, ${ubereats})`;
}).join(',\n');

const sql = `-- Delivery URL import — generated by scripts/import-delivery-urls.js
-- ${deliveryRows.length} listings with Deliveroo and/or Uber Eats URLs
-- Run this in the Supabase SQL editor.

-- Temporarily disable audit trigger to allow service-level bulk updates
ALTER TABLE listings DISABLE TRIGGER ALL;

UPDATE listings AS l
SET
  delivery_deliveroo = v.deliveroo_url,
  delivery_ubereats  = v.ubereats_url
FROM (VALUES
${valuesList}
) AS v(id, deliveroo_url, ubereats_url)
WHERE l.id = v.id;

-- Re-enable triggers
ALTER TABLE listings ENABLE TRIGGER ALL;

-- Verify
SELECT name, delivery_deliveroo, delivery_ubereats
FROM listings
WHERE delivery_deliveroo IS NOT NULL OR delivery_ubereats IS NOT NULL
LIMIT 10;
`;

const outPath = './scripts/upload-delivery-urls.sql';
fs.writeFileSync(outPath, sql, 'utf-8');

console.log(`✅ SQL file written to: ${outPath}`);
console.log(`   ${deliveryRows.length} UPDATE rows`);
console.log(`\n👉 Next step: paste ${outPath} into the Supabase SQL editor and run it.`);

